public class VariablesAndDataType {
    public static void main(String[] args) {
        // Declare variables of different data types
        int _myInt = 10; // Integer data type
        double $myDouble = 20.5; // Double data type
        char myChar = 'A'; // Character data type
        String myString = "Hello, World!"; // String data type
        boolean myBoolean = true; // Boolean data type

        // Print the values of the variables
        System.out.println("Integer value: " + _myInt);
        System.out.println("Double value: " + $myDouble);
        System.out.println("Character value: " + myChar);
        System.out.println("String value: " + myString);
        System.out.println("Boolean value: " + myBoolean);


        /**
 * Java Rules or Naming Conventions for Variables:
 * Primitive data types in lowercase
 * variable name start with lower case letter
 * variable should not be start numeric value
 * variable name should not contain spaces
 * variable name should not be a Java reserved keyword
 * special characters are not allowed except _ and $
 * you can start name with _ and $
 * unique names for each variable
 * 25-30 characters are allowed
 */

    }
}

